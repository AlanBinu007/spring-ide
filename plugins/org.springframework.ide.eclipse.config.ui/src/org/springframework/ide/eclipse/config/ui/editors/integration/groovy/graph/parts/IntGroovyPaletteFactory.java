package org.springframework.ide.eclipse.config.ui.editors.integration.groovy.graph.parts;

import java.util.ArrayList;
import java.util.List;

import org.eclipse.gef.palette.CombinedTemplateCreationEntry;
import org.eclipse.gef.palette.PaletteDrawer;
import org.eclipse.gef.palette.PaletteEntry;
import org.springframework.ide.eclipse.config.core.schemas.IntGroovySchemaConstants;
import org.springframework.ide.eclipse.config.graph.model.AbstractConfigGraphDiagram;
import org.springframework.ide.eclipse.config.graph.model.ModelElementCreationFactory;
import org.springframework.ide.eclipse.config.graph.parts.IPaletteFactory;
import org.springframework.ide.eclipse.config.ui.editors.integration.graph.IntegrationImages;
import org.springframework.ide.eclipse.config.ui.editors.integration.groovy.graph.model.ControlBusModelElement;


/**
 * @author Leo Dos Santos
 */
public class IntGroovyPaletteFactory implements IPaletteFactory {

	public PaletteDrawer createPaletteDrawer(AbstractConfigGraphDiagram diagram, String namespaceUri) {
		PaletteDrawer drawer = new PaletteDrawer("", IntegrationImages.BADGE_SI_GROOVY); //$NON-NLS-1$
		List<PaletteEntry> entries = new ArrayList<PaletteEntry>();

		CombinedTemplateCreationEntry entry = new CombinedTemplateCreationEntry(
				IntGroovySchemaConstants.ELEM_CONTROL_BUS,
				Messages.IntGroovyPaletteFactory_CONTROL_BUS_COMPONENT_DESCRIPTION, new ModelElementCreationFactory(
						ControlBusModelElement.class, diagram, namespaceUri), IntegrationImages.CONTROL_BUS_SMALL,
				IntegrationImages.CONTROL_BUS);
		entries.add(entry);

		drawer.addAll(entries);
		return drawer;
	}

}
